    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jComboBox1.getSelectedIndex();
        String PatientToDelete = jComboBox1.getItemAt(selectedIndex);
        int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete " + PatientToDelete + "'s record?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (confirmation == JOptionPane.YES_OPTION) {
                boolean removed = false;
                for (Patient patient : Patient_List) {
                    patient.generateCName();
                   if (patient.getCName().equalsIgnoreCase(PatientToDelete)) {
                      Patient_List.remove(patient);
                      removed = true;
                      break;
                   }
                }
                if (removed) {
                    
                    model.removeRow(selectedIndex);
                    model1.removeRow(selectedIndex);
                    
                    Save_To_Database();
                    JOptionPane.showMessageDialog(null, "Patient successfully removed from the database!");
                } 
                else {
                    JOptionPane.showMessageDialog(null, "Patient not found!");
                }
                this.dispose();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

