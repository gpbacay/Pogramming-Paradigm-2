    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete all records?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (confirmation == JOptionPane.YES_OPTION) {
                
                Patient_List.clear();
                model.setRowCount(0);
                Patient_List.clear();
                model1.setRowCount(0);
                
                Save_To_Database();
                JOptionPane.showMessageDialog(null, "Patient records successfully cleared!");
                this.dispose();
            }
    }//GEN-LAST:event_jButton3ActionPerformed

